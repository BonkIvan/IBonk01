package by.belhard.J20.bonk.lessons.lesson04;

public class Country {

    String  title;
    int zipCod;

    public Country(String title, int zipCod) {
        this.title = title;
        this.zipCod = zipCod;
    }

    public String getTitle() {
        return title;
    }

    public int getZipCod() {
        return zipCod;
    }

}
